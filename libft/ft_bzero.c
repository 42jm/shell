#include <libft.h>

void	ft_bzero(void *s, size_t n)
{
	char	*sbyte;
	size_t	i;

	sbyte = (char *)s;
	i = 0;
	while (i < n)
	{
		sbyte[i] = '\0';
		i++;
	}
}
