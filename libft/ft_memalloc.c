#include <libft.h>

void	*ft_memalloc(size_t sz)
{
	char	*m;
	size_t	i;

	m = (char *)malloc(sz);
	i = -1;
	if (m)
		while (++i < sz)
			m[i] = 0;
	return ((void *)m);
}
